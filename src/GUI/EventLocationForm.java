    private void tabelaLoad() {
        List<EventLocation> lista = eventLocationRepository.findAll();
        eventLocationModel.add(lista);
        LocationTable.setModel(eventLocationModel);
        eventLocationModel.fireTableDataChanged();
        tabelaSelectedIndexChange();
    
    }
    
    private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM = LocationTable.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    
                    EventLocation eventLocation = eventLocationModel.getEventLocation(selectedIndex);
                    City city = (City)eventLocation.getCityID();
                    Event event = (Event)eventLocation.getEventID();
                    
                    txtStreet.setText(eventLocation.getStreet());
                    txtDescription.setText(eventLocation.getDescription());
                    
                    
                    
                    CountryCombo.setSelectedItem(city.getCountryID());
                    CountryCombo.repaint();
                    
                    CityCombo.setSelectedItem(city);
                    CityCombo.repaint();
                   
                    EventCombo.setSelectedItem(event);
                    EventCombo.repaint();
                    
                    
                }
            }
        });
    }
    
    
