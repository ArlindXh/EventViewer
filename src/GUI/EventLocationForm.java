    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
         try {
            
            
            int row = LocationTable.getSelectedRow();
            if (txtStreet.getText().equals("") ||txtDescription.getText().equals("") 
                    || CountryCombo.getSelectedItem() == null || CityCombo.getSelectedItem() == null
                    || EventCombo.getSelectedItem() == null )
            {
                JOptionPane.showMessageDialog(this, "Ju lutem Mbusheni gjitha kolonat :", "Error", JOptionPane.ERROR_MESSAGE);
                if(CountryCombo.getSelectedItem() == null )
                    lblCountry.setText( " Selekto nje shtet ! ");
                if(CityCombo.getSelectedItem() == null )
                    lblCName.setText(" Selekto nje Qytet ");
                if(EventCombo.getSelectedItem() == null ) 
                    lblEvent.setText(" Selekto nje Event ! ");
                if(txtStreet.getText().equals(""))
                    lblStreet.setText(" Mbyshni te dhenat e Rruges ! ");
                if(txtDescription.getText().equals(""))
                    lblDescription.setText(" Shkruani te dhena ne fushen Description ! ");
                    
                    
                
            } 
            else{
                lblCountry.setText( "");
                lblCName.setText("");
                lblEvent.setText("");
                lblStreet.setText("");
                lblDescription.setText("");
               
                if(row == -1)
                {
                    Event event = eventI.findById(3);
                    EventLocation eventLocation = new EventLocation();
                    City city = (City)CityCombo.getSelectedItem();
                     eventLocation.setCityID(city);
                     eventLocation.setCityID(city);
                     eventLocation.setEventID(event);
                    
                    
                    
                    
                    eventLocation.setStreet(txtStreet.getText());
                    eventLocation.setDescription(txtDescription.getText());
                    

                    eventLocationRepository.create(eventLocation);
                    
                    
                    JOptionPane.showMessageDialog(this, "E dhëna u ruajt me sukses !");
                } 
                else 
                {
                    EventLocation eventLocation = this.eventLocationModel.getEventLocation(row);
                    
                    Event event = (Event) EventCombo.getSelectedItem();
                    
                    City city = (City)CityCombo.getSelectedItem();
                    eventLocation.setCityID(city);
                    eventLocation.setEventID(event);
                    
                    
                    eventLocation.setStreet(txtStreet.getText());
                    eventLocation.setDescription(txtDescription.getText());
                    
                    
                    
                    
                    eventLocationRepository.edit(eventLocation);
                    JOptionPane.showMessageDialog(this, "E dhëna u editua me sukses");
                }
                emptyObject();
                tabelaLoad();
            }
              
        }   catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
    }//GEN-LAST:event_btnSaveActionPerformed

