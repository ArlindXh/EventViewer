   public String getCountryOrdered()
        {
        StringBuilder sb = new StringBuilder();
        CountryRepository cP =  new CountryRepository();
        List<Country> listC = cP.findAllOrdered();
        for(int i=0;i<listC.size();i++)
            sb.append(listC.get(i).getCountryName() + " \n");
        
        return sb.toString();
        }
    
